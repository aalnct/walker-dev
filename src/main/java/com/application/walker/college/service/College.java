package com.application.walker.college.service;

/**
 * @author AmitAgarwal
 *
 */
public class College {
	
	private String collegeName;
	private String deanName;
	private String facultyName;
	private String facultyEmailAddress;
	private Integer collegeIdentificationCode;
	
	public String getCollegeName() {
		return collegeName;
	}
	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}
	public String getDeanName() {
		return deanName;
	}
	public void setDeanName(String deanName) {
		this.deanName = deanName;
	}
	public String getFacultyName() {
		return facultyName;
	}
	public void setFacultyName(String facultyName) {
		this.facultyName = facultyName;
	}
	public String getFacultyEmailAddress() {
		return facultyEmailAddress;
	}
	public void setFacultyEmailAddress(String facultyEmailAddress) {
		this.facultyEmailAddress = facultyEmailAddress;
	}
	public Integer getCollegeIdentificationCode() {
		return collegeIdentificationCode;
	}
	public void setCollegeIdentificationCode(Integer collegeIdentificationCode) {
		this.collegeIdentificationCode = collegeIdentificationCode;
	}
	

}
